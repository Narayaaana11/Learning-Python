# Arithmetic Operators

friends = 10
print("Initial number of friends:", friends)

# Addition (+)
friends = friends + 1
print("After friends = friends + 1:", friends)

# Addition assignment (+=)
friends += 1
print("After friends += 1:", friends)

# Subtraction (-)
friends = friends - 2
print("After friends = friends - 2:", friends)

# Subtraction assignment (-=)
friends -= 2
print("After friends -= 2:", friends)

# Multiplication (*)
friends = friends * 3
print("After friends = friends * 3:", friends)

# Division (/)
friends = friends / 2
print("After friends = friends / 2:", friends)

# Exponentiation (**)
friends = friends ** 2
print("After friends = friends ** 2:", friends)

# Exponentiation assignment (**=)
friends **= 2
print("After friends **= 2:", friends)

# Modulus (%) â†’ gives the remainder
remainder = friends % 2
print("Remainder when friends is divided by 2:", remainder)
